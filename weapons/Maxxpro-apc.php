            The International MaxxPro MRAP is an armored fighting vehicle designed by American company Navistar International's subsidiary Navistar Defense along with the Israeli Plasan Sasa, who designed and manufactures the vehicle's armor. MRAP vehicles are designed from the outset for blast-resistance against land mines and even car bombs. These vehicles normally serve a variety of roles including as armored personnel or weapons carrier, convoy protection platform, key leader vehicle and command post and armored ambulance.
