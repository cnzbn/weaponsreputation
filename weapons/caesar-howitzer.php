                <p class="font"> <br> CAESAR Howitzer LG1 105mm Towed Gun (FR)<br><br>
               The LG1 105mm Towed Gun, manufactured by Nexter, can fire salvos of six rounds in less than 15 seconds at ranges p 17 kilometers with good accuracy. Suited for rapid reaction forces, requiring a highly deployable fire support capability with limited maintenance requirement, the LG1 is able to operate in GPS denied environment.
