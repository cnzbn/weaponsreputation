        <div class="col-12 col-sm-10 col-md-8 nopadding margin">       
        <p class="font"> 
             Saudi Arabia and the UAE have stated their aims to pursue world-class weapons production capabilities.
In both instances they have sought to jump-start the process by launching joint ventures with established
firms and by creating national  companies (SAMI in Saudi Arabia and EDGE in the UAE) to
coordinate these markets. The Saudis have a very challenging target to meet: Vision 2030’s aim is to
produce 50 percent of all defense materials in the kingdom. Most arms producing nations fall into this
category. It includes most of the NATO nations as well as established arms producers such as Turkey
and Brazil. Countries with this level of expertise can produce capable aircraft,
armored vehicles, adaptable artillery systems such as the French CAESAR, and a variety of missiles.

            <span class="caption-gray"><a href="http://arabcenterdc.org/policy_analyses/can-saudi-arabiaand-
