        <p class="font"> 
            In July 2006, Giat Industries (now Nexter) announced an export contract for 76 of its CAESAR artillery systems. While Giat would not confirm the customer, Agence-France Presse reported that they were destined for Saudi Arabia. Since the July 21/06 defense cooperation agreement signed with France, a number of deals to Saudi Arabia have fallen through or been delayed indefinitely. In September 2011, Saudi Arabia placed an order worth $210 million for an additional 32 CAESAR weapon systems. Deliveries under order reportedly began in late 2013. This order was in addition to the 100 CAESAR systems already fielded by the Saudi Arabian National Guard. Nexter reportedly completed final deliveries under the follow-on order during the first half of last year. The French government continues to supply the weapon: no less than 129 CAESARs are due to be delivered to Saudi Arabia between now and 2023. <span class="caption-gray"><a href="https://www.armyrecognition.com/april_2018_global_defense_security_army_news_industry/extended_production_of_french_nexters_caesar_155mm_self-propelled_howitzer.html" target="blank"> (armyrecognition.com, Apr. 2018)</a></span><br></p>           
