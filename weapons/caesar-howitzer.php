        <p class="font"> 
            The Caesar is a 155 mm/52-calibre gun-howitzer installed on a 6X6 truck chassis. The CAESAR platform was developed by the former GIAT Industries (now known as Nexter) and is operated by French and other armies since 2009 in Afghanistan, Lebanon, Mali and current operation in Sahel, Iraq, in Middle-East and East Asia. 
