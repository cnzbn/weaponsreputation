            <p class="font"> <br> RIM-162 Evolved Seasparrow Missile (ESSM) (US)<br><br>
                The RIM-162 Evolved Sea Sparrow Missile is designed to counter supersonic maneuvering anti-ship missiles. ESSM also has the ability to be "quad-packed" in the Mark 41 Vertical Launch System, allowing up to four ESSMs to be carried in a single cell.
                Developed by the U.S. Navy and nine of the other 11-member nations of the NATO SEASPARROW Consortium, the ESSM missile is bringing transformational anti-ship missile defense capabilities to NATO and other allies (Australia, Belgium, Canada, Denmark, Germany, Greece, the Netherlands, Norway, Portugal, Spain, Turkey and the United States). 
