            <p class="font"> <br> Sea Giraffe AMB (Giraffe Agile Multi Beam) Air Search Radar (SE)<br><br>
                Giraffe AMB provides accurate real-time air target information. Giraffe AMB monitors the air volume 360° for air targets from supersonic fighters, cruise missiles and helicopters, down to small, slow and low-flying UAVs. It is simultaneously capable to locate and warn for incoming rockets, artillery and mortar rounds. The radar gets its name from the distinctive folding mast which when deployed allows the radar to see over nearby terrain features such as trees, extending its effective range against low-level air targets.

 
