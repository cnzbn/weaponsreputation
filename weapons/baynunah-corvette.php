        <p class="font"> 
            In January 2004, the United Arab Emirates’ Ministry of Defence in Abu Dhabi awarded a contract to Abu Dhabi Shipbuilding (ADSB) for a new class of multipurpose missile corvette, called Baynunah. The Baynunah class is designed for “patrol and surveillance, minelaying, interception and other antisurface warfare operations in the United Arab Emirate’s territorial waters and exclusive economic zone. This class is based on the CMN Group’s Combattante BR70 design. The corvettes are named after the Baynunah region of Abu Dhabi. Construction of the first vessel began in May 2005 and launched in June 2009 at CMN’s Cherbourg shipyard. 
            <span style="font-gray"><a href="https://www.naval-technology.com/projects/baynunah/"  target=_blank> &#x2192; naval-technology.com/baynunah</a></span></p>
