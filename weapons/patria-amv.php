              Finnish state-controlled Patria Group has secured an export license to sell an unspecified number of 8x8 AMV armored vehicles to the United Arab Emirates (UAE). Patria, which described the deal as significant, said financial details around the contract would remain classified. The delivery is expected to be within the range of 40 to 90 units, if options for an additional 50 units on the suspected core 40-unit order are exercised. The contract price agreed with the UAE excludes weapons systems. A large part of the order will be produced under license by Rosomak SA, Patria's cooperation partner in Poland that delivers hulls for new Patria AMV contracts. About 50% of all Patria 8x8 AMVs are manufactured in Poland. By taking advantage of Poland's lower labor costs, Patria is able to optimize production-related savings and pitch more competitive bids for its armored vehicle offerings.
