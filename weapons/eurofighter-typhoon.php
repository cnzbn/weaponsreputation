        <p class="font"> 
            The Eurofighter Typhoon is the world's most modern swing-role fighter. It can carry up to six bombs and six missiles, a cannon and a targeting pod, with sufficient processing power to simultaneously support missile in-flight updates and bomb in-flight targeting. Combining a proven, agile airframe built from stealth materials with the latest sensor, control and weapons systems delivers optimum combat capability – both beyond visual range (BVR) and in close combat.
        <br><br>
            Eurofighter Typhoon is built on a consortium of 4 European nations: United Kingdom, Germany, Italy and Spain, and their leading aerospace and defence companies. While Eurofighter Jagdflugzeug GmbH coordinates the programme on Industry side, the NATO Eurofighter and Tornado Management Agency (NETMA) acts as the single point of contact for customers and governments. Each partner company assembles its own national air force, but builds the same parts for all aircraft (including exports).
