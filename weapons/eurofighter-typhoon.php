        <div class="col-12 col-sm-10 col-md-8 nopadding ">       
        <p class="font"> 
             Saudi Arabia is a major buyer of UK weapons, and has been since the 1960s. The UK government is intimately involved in these deals as they are operated through Government-to-Government contracts. These contracts are complemented by those between the UK government and the prime contractors. The Al Yamamah (dove) agreements of the mid-1980s were between the Thatcher government in the UK and the Saudi Arabian government. Military equipment, especially Tornado and Hawk jets, were to be supplied by what is now BAE Systems. The deals also included servicing, spares and ancillary services. Al Yamamah was originally designed as a barter agreement with Saudi Arabia paying in oil. However, an oil price crash in the mid-1980s meant that the UK government had to use its Export Credits Guarantee Department (ECGD), now UK Export Finance, to underwrite the deal for £1 billion. The Bank of England and the Treasury had doubts about these arrangements. In 2007, BAE sold 72 of its Eurofighter Typhoons to Saudi Arabia in a £4.4 billion deal called The Salam Project.  Outstanding price issues were finally agreed in February 2014 after a visit from Prince Charles. The Typhoons replace the Air Defence Variant Tornados supplied under the Al Yamamah deals; the remaining Tornados continue to be upgraded and serviced under what is now called Saudi British Defence Co-operation Programme.
            <span class="caption-gray"><a href="https://caat.org.uk/resources/countries/saudi-arabia/" target="blank"> (caat.org.uk)</a>
            </span></p>
            
