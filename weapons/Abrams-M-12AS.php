            Saudi Arabia requested to buy General Dynamics Land Systems-made M1A2S Abrams tanks and M88A1/A2 Heavy Equipment Recovery Combat Utility Lift Evacuation System (Hercules) Armored Recovery Vehicles (ARV) in a foreign military sale worth approximately $1.15 billion, according to Defense Security Cooperation Agency.  The kingdom's request includes up to 153 M1A1/A2 tanks for conversion to 133 M1A2S Saudi Abrams-configured main battle tanks, along with 20 replacements for vehicles damaged in battle. In addition to the vehicles, Saudi Arabia is requesting 153 M2 50 caliber machine guns, 266 7.62mm M240 machine guns and 153 smoke grenade launchers.<span class="caption-gray"><a href="https://www.defensenews.com/home/2016/08/09/saudi-arabia-requests-us-tanks-vehicles-for-1-15-billion/" target="blank"> (defensenews.com, Aug. 8, 2019)</a>
