        <p class="font"> 
            The U.S. Air Force program office responsible for F-15 foreign military sales expects to award contracts late next year valued at $2.5 billion for maintenance support of the Royal Saudi Air Force (RSAF) F-15 fleet. The service has invited both U.S. and foreign-owned companies to bid on the “sustainment” work, which has until now been performed by a Saudi-owned firm (Riyadh-based Al-Raha Group for Technical Services). Changing that practice, the Saudi government has asked the U.S. to make the work available to other companies. The work includes printing technical orders, stocking depots and work benches, repairing aircraft and support equipment and providing personnel to support supply activities in Saudi Arabia.
