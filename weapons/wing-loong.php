            </span><br><br>
            Despite strong sales, however, there are questions about the quality of Chinese systems. One area of weakness lies in turboprop engines – engines are a persistent problem with Chinese-produced aircraft. This shows up in performance. The MQ-9B Reaper, for example, has published service ceiling of 49,000ft, while that for the Wing Loong II is 29,500ft. The Reaper is also faster, with a top speed of 260kt (480km/h), compared with 199kt for the Chinese aircraft. A Wing Loong II likely costs a fraction of an MQ-9B. A ChinaPower report on Chinese UAV technologies indicates that a Wing Loong II likely costs $1-2 million, compared with $16 million for the Reaper. Experts also question the datalinks of Chinese systems, suggesting that they are not as robust as those available for similar western platforms. <span class="caption-gray"><a href="https://www.flightglobal.com/military-uavs/china-finds-its-uav-export-sweet-spot/132557.article" target="blank"> (flightglobal.com, Jun. 2019)</a></span></p>
            
