        <div class="col-12 col-sm-10 col-md-8 nopadding margin ">       
        <p class="font"> 
            The Airbus A330 MRTT is an aerial refuelling tanker aircraft based on the civilian Airbus A330. It is designed as a dual-role air-to-air refuelling and transport aircraft. The A330 MRTT can carry up to 111 tonnes of fuel, a maximum payload of up to 45 tonnes and support the deployment of four fighter aircraft plus 50 personnel and 12 tonnes of freight (luggage, spare parts and equipment) in one direct flight over 5,200 km. Standard commercial A330-200s are delivered from Airbus Final Assembly Line in Toulouse, France to
Airbus Military Conversion Centre in Getafe, Spain for fitting of refuelling systems and military avionics.
            <span style="font-gray"><a href="https://www.airbus.com/defence/a330mrtt.html"  target=_blank> &#x2192; airbus.com/a330mrtt</a></span></p>
