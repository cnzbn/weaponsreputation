        <div class="col-12 col-sm-10 col-md-8 nopadding margin">       
        <p class="font"> 
             Saudi A330 MRTTs are refueling planes that allow for bomber jets to refuel mid-air Saudi jets flying
over Yemen. Spain’s economy ministry reported that in 2016 more than €101 million euro of spare
parts for in-flight refueling aircrafts were exported to Saudi Arabia. Both Iberia and Airbus confirmed
checks and maintenance were done in Saudi Arabia, but did not want to specify the maintenance work
conducted in Saudi Arabia or in Spain. The legally-binding Common Position prohibits maintenance
of military equipment for countries that commit atrocities.
