        <p class="font"> 
             After many years of neglect, an increasing number of users are now placing growing emphasis on their
mortar systems, as for most infantry battalions these are the only indirect fire weapons under their direct
control and can therefore rapidly respond to calls for fire support. Recent mortar developments include
120mm turret-mounted mortars which can be integrated onto tracked and wheeled armoured platforms,
and can be used for both the direct and indirect fire missions. There is also a new generator of mortars that
can be installed on light cross-country vehicles. The Spanish NTGS ALAKRAN Light Mortar Carrier (LMC) is already in service in
the Middle East integrated on a Toyota LAND CRUISER 4×4 platform, with the Ukraine also deploying the
system integrated into a BARS 8 4×4 armoured personnel carrier.
            
