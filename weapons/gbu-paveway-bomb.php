        <div class="col-12 col-sm-10 col-md-8 nopadding ">       
        <p class="font"> 
            Weapon sales from United Kingdom to Saudi Arabia were banned in June 2019 after a Court of Appeal ruled that the government may have contravened international humanitarian law by approving weapon sales to the Saudis that might have been used in the civil war in Yemen. Britain is one of the largest exporters of defense equipment in the world, largely thanks to Saudi Arabia’s purchase over more than 30 years of Tornado and Typhoon combat jets as well as Hawk jet trainers. Raytheon Paveway IV precision-guided bombs, partly built in the U.K., are also among the list of recent significant sales to Saudi Arabia. In an action brought by anti-arms trade campaigners, the court ruling forced the British government to reassess whether previous export licenses had been issued on the correct legal basis, given alleged violations of international humanitarian law by the Saudi military, specifically reported airstrikes that hit civilian targets.<span class="caption-gray"><a href="https://www.defensenews.com/global/europe/2020/07/07/britain-lifts-ban-on-saudi-weapons-exports/" target="blank"> (defensenews.com, Jul. 7, 2020)</a>
            </span><br><br>
            
             The United States exports the Paveway kits to more than 30 countries in the world but Saudi Arabia and UAE (part of the coalition fighting in Yemen) are its first costumers. Saudi Arabia ordered 3100 Paveway kits in 2011, delivered between 2016 and 2018, and other 8120 kits as part of a $1.29 billion deal signed in November 2015, agreed during the state visit of Saudi Arabia’s King Salman bin Abdulaziz Al Saud with former President Barack Obama on September 4th, 2015. Salman became the new King of Saudi Arabia on 23 January 2015 following the death of his half-brother, King Abdulla. Since then he has strengthened the relationship with the White House.
            <span class="caption-gray"><a href="https://www.reuters.com/article/us-saudi-usa-arms-idUSKCN0T51NC20151116" target="blank"> (reuters.com, Nov.16, 2015)</a>
            </span><br><br>
