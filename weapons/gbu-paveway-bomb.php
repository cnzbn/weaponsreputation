        <div class="col-12 col-sm-10 col-md-8 nopadding margin">       
        <p class="font"> 
            An emergency authorization declared by the Trump administration allows Raytheon Company to team with the Saudis to build high-tech bomb parts in Saudi Arabia. The move grants Raytheon and the Saudis sweeping permission to begin assembling the control systems, guidance electronics and circuit cards that are essential to the company’s Paveway smart bombs. The United States has closely guarded such  technology for national security reasons. The new arrangement is part of a larger arms package, previously blocked by Congress, that includes 120,000 precision-guided bombs that Raytheon is prepared to ship to the coalition. These will add to the tens of thousands of bombs that Saudi Arabia and the United Arab Emirates have already stockpiled, and some in Congress fear the surplus would let the countries continue fighting in Yemen long into the future. The move also includes support for Saudi F-15 warplanes, mortars, anti-tank missiles and 50-caliber rifles. 
            <span class="caption-gray"><a href="https://www.nytimes.com/2019/06/07/us/saudi-arabia-arms-sales-raytheon.html" target="blank"> (nytimes.com, Jun. 7, 2019)</a>
            
