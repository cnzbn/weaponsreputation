        <p class="font"> 
             Canada and Saudi Arabia reached in an agreement in February 2014 under which Saudi Arabia would purchase
C$15 billion worth of military equipment from Canada. According to the CBC, this deal included 928 light
armoured vehicles, specifically, a model known as LAV-6. Among these LAV-6s, 119 were categorized
as heavy assault. Also included under this deal is a 14-year provision of technical assistance. This deal
solidified Saudi Arabia as the second-largest export destination for Canadian arms after the US. 
