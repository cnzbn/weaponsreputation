             The Qasef-1 not only shares near-identical design and construction characteristics with the Iranian Ababil UAV, but also features identical serial number prefixes. These features suggest that the Qasef-1 is an Iranian designed variant of the Ababil-CH or Ababil-T.
Second, the interception of six Qasef-1 UAVs after reportedly transiting Oman—a known smuggling route for Iranian materiel support to Houthi and Saleh-aligned forces  in Yemen—also suggests that the Qasef-1 is imported, rather than designed or manufactured in Yemen. UAE forces report that Houthi and Saleh-aligned forces employ the Qasef-1 to target Coalition MIM104 ‘Patriot’ surface-to-air missile systems. They do so by crashing the UAVs into the systems’ radar sets directing the UAVs by programming their systems with open-source GPS coordinates of the Patriots’ positions.  
The use of these UAVs illustrates the Houthi's ability to employ low-cost technology against the Coalition’s sophisticated
military assets. Their acquisition of Iraniandesigned Qasef-1 UAVs supports allegations that Iran continues to bolster the capacity of Houthi and
Saleh-aligned forces through the transfer of new technology and advanced weaponry.
