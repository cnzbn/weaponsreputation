             US armoured vehicle manufacturer Oshkosh Defence has set up a joint venture, Oshkosh Al Tadrea
            Manufacturing (OTM) with Saudi Arabia’s Al Tadrea Manufacturing, of which the latter will be the
            majority shareholder, in order to establish a local manufacturer for wheeled tactical vehicles. The Saudi kingdom will hold the largest property share, according to a report by the Saudi Press Agency. OTM will “develop manufacturing expertise to become a major partner for wheeled tactical vehicle services, [and] it will also provide the necessary services to its customers in the security and military sectors in the Kingdom, through an integrated and sustainable support fleet, as well as activating the supply chains more broadly and more efficiently,” the report read.
