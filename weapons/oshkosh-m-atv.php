             The United Arab Emirates Army initially ordered 55 M-ATVs through a FMS sale in 2011. The UAE ordered another 750 M-ATVs direct from Oshkosh in July 2012. These are to provide greater off-road mobility and crew protection for regional security and peace-keeping operations; users include the elite Presidential Guard. Deliveries were completed in August 2013. In September 2014, the UAE requested another 44 M-ATVs from U.S. surplus stocks. In September 2013, the Saudi Arabian Army began negotiations for an order for an undisclosed number of M-ATVs. Saudi Arabia received an estimated 450 M-ATVs including some Extended Wheelbase variants.
