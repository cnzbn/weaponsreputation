             The Houthis’ most pressing objective has been to force an end to the Arab coalition’s military intervention
and survive as the de facto governing power in Yemen. The Houthis have used missile and drone attacks to
serve these goals by raising the military and economic costs for the coalition to continue its intervention.
They have also used missile attacks as a political and propaganda tool to shore up domestic support and
weaken the morale of the Saudi civilian populace.
Objectively, the Houthi use of its longer-range ballistic missiles, such as the Burkan series, has produced
limited military success. Of hundreds of launches, only seven resulted in significant coalition losses. Missile
and drone attacks against Saudi economic targets have also failed to meaningfully impact Saudi oil production
or rattle oil markets in a lasting way. Yet the Houthis’ determined use of ballistic missiles has had
secondary effects. Attacks into Saudi Arabia have tied-up coalition airpower into extensive “Scud-hunting”
efforts, resources it could have employed elsewhere. While limited in kinetic impact, Houthi missile attacks
against civilian areas deep in Saudi Arabia have been a source of political embarrassment for the Saudi
government and raised the stakes for it to find a speedy resolution to the conflict. As Houthi attacks on oil
infrastructure have become more precise, the potential for them to cause greater havoc on production
may have also increased the urgency for Saudi Arabia to find an off-ramp from the conflict.
