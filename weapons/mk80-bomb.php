            <p class="font">
            Rheinmetall's Italian subsidiary, RWM Italia, produces bombs that are exported to Saudi Arabia and used to bomb Yemen. During the two-year period of 2012 to 2013, RWM Italia issued export licenses for MK82 and MK83 aerial bombs intended to be exported to Saudi Arabia, for a total value of over €86 million. However, it is impossible to know how many and which bombs were exported from Italy to Saudi Arabia in the last two years. The reports submitted to the Parliament indicate only the total value of export licenses to individual countries and generic description of the weapons (ammunition, land vehicles, ships, aircraft, etc.). It is reported that in 2015 RWM Italia was issued 24 licenses for a total value of over €28 million, but the countries of destination are not listed.
            In the two-year period 2014-15, the Ministry of Foreign Affairs has authorized the export to Saudi Arabia of a military arsenal for a total value of nearly €420 million. These include “automatic weapons” that can be used for internal repression; “ammunition;” “bombs, torpedoes, rockets and missiles;” “target acquiring equipment;” “explosives;” “aircraft,” including components for the Eurofighter “Al Salam,” the Tornado “Al Yamamah” and the EH-101 helicopters; “electronic equipment” and “specialized equipment for military training.” During this two-year period, military systems and materials for more than €478 million were delivered to the Saudi Royal Armed Forces.
               
