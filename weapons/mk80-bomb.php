        <p class="font"> 
            The MK80 bomb serie list four bombs, ranging from the 113 kg Mk 81 to the 907 kg Mk 84. The Mk 80 series of bombs was developed in the late 1940s, and remains in service today; low-drag, general-purpose aircraft bombs contain 89 kg of high explosive. Originally dropped as an unguided bomb (sometimes referred to as an ‘iron’ or ‘dumb’ bomb), early versions were only able to hit their target 5.5% of the time, requiring large numbers of bombs to be dropped. 
