            <span class="caption-gray"><a href="https://micheletocci.wordpress.com/2016/10/16/italys-dirty-war-in-yemen/" target="blank"> (Italy's Dirty War in Yemen, Oct. 2016)</a> </span></p>
            
            <p class="font"> 
             On 31 July 2019, as a response to the pressure of civil society organisations and critical shareholders, the Italian government has decided to suspend RWM Italia’s export licensed to Saudi Arabia and United Arab Emirates for 18 months. On 12 September RWM Italia has declared that, following the decision of the Italian government, it will cut 160 jobs (out of 200) in its plant of Domusnovas, in Sardinia, where the bombs are produced. An area with high unemployment where the dismissed workers will find it difficult to find new jobs.
