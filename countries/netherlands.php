        <p class="font"> <span style="margin-left:4em">ARMS TRADE TREATY</span><br>
       Ratified and entried in force since 24 December 2014. <a href="http://www.armstrade.info/national-transfer-control/netherlands/" target="blank"> &#x2192; ATT Netherlands</a><br></p>
 
        <p class="font"> <span style="margin-left:4em">TRANSPARENCY</span><br>
            Arms Export Reports available at:  <a href="https://www.stopwapenhandel.org/informatie/Overheid/overheid" target="blank">&#x2192; Stop Wapenhandel</a> <a href="https://www.rijksoverheid.nl/onderwerpen/exportcontrole-strategische-goederen" target="blank">&#x2192; Export Control of Strategic Goods</a><br></p>
        
        <p class="font"> <span style="margin-left:4em">POSITION TOWARDS THE CONFLICT </span><br>
             The Dutch parliament has voted to ban arms exports to Saudi Arabia in protest against the kingdom’s humanitarian and rights violations. It sees the Netherlands become the first EU country to put in practice a motion by the European Parliament in February 2016 urging a bloc-wide Saudi arms embargo. The bill quoted UN figures which suggest almost 6,000 people - half of them civilians - have been killed since Saudi-led troops entered the conflict in Yemen. According to Reuters, the Dutch bill asks the government to implement a strict weapons embargo that includes dual-use exports which could potentially be used to violate human rights.<br>
            <span class="caption-gray">
                    (<a href="https://www.independent.co.uk/news/world/europe/netherlandsvotes-to-ban-weapons-exports-to-saudi-arabia-a6933996.html" target="blank">Independent, Mar. 2016)</a><br>
            </span>
        </p>
        
             
        <p class="font"><span style="margin-left:4em">In July 2019 the Minister for Foreign Trade and Development Cooperation Sigrid Kaag decided, on the basis of new information, to remove previously imposed restrictions on Egypt. For the other countries in question (Saudi Arabia and the United Arab Emirates) the restrictions will remain in place. Since 27 November 2018 a presumption of denial was instituted on arms exports to the UAE and Egypt. This presumption of denial had previously been introduced for Saudi Arabia. The more restrictive policy was prompted by a report by the Group of International and Regional Eminent Experts on Yemen which posited a link between the navies of the coalition countries and violations of human rights and international humanitarian law.</span><br> 
                 
             <span style="margin-left:4em"> In this context, ‘presumption of denial’ means that an export licence for military goods will not be issued for these end users unless it has been incontrovertibly demonstrated that the goods will not be used in the conflict in Yemen. In contrast with normal policy on assessing the risk of arms exports, the burden of proof under the new policy is thus reversed. The government is closely monitoring the situation in Yemen. New information about the involvement of the Egyptian navy in the operation in Yemen suggests that the navy is being deployed mainly to secure the waterways around the Suez Canal. There is no reason to assume that Egypt is involved in the maritime blockade of Yemen. This is why the presumption-of-denial policy was lifted for Egypt.
             </span><br>
            
                <span class="caption-gray">
                    (<a href="https://www.government.nl/documents/publications/2019/08/01/presumption-of-denial-for-countries-engaged-in-the-yemen-conflict" target="blank">Dutch Government, Aug. 2019)</a>
                </span>
            </p>
        
                
