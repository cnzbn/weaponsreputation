<div class="col-12 col-sm-10 col-md-6 col-lg-6">
    <p class="font">The UAE used the M109 Howitzer in Yemen. Propelled grenades fired by these howitzers have a range of 35 kilometers and can be used for heavy shelling at long distance. A tactic with an enormous risk of collateral damage. The Netherlands sold 87 of these heavy self-propelled artillery systems (M109A2/A3 version or M109L47) to the UAE in 1997, after an RDM upgrade. There are still 858 or 879 in use. (Jordan also bought M109 howitzers from the Netherlands, but is not involved in ground operations in Yemen.) The sale of the howitzers to the UAE itself may be a long time ago; however, according to the last published reports the Netherlands exported components for M109 howitzers as
recent as 2014. Also, in April 2006 and in December 2011, 52200 grenades of this calibre from Germany
left Flushing (Vlissingen) by a ship heading for the UAE. <br>
