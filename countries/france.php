        <p class="font"> <span style="margin-left:4em">POSITION TOWARDS THE CONFLICT </span><br>
             France urges all the parties to the conflict in Yemen to respect international humanitarian law in the conduct of hostilities – in the first place, the principles of distinction between civilians and combatants and proportionality. <br>
            
            <span style="margin-left:4em">It is an issue France is tackling with members of the coalition, which, as party to an armed conflict, has responsibilities in this respect. It has also urged all the parties to carry out transparent, impartial and credible investigations into violations of international humanitarian law and human rights in Yemen, in accordance with international standards, and bring the perpetrators to justice. </span><br> 
            
            <span style="margin-left:4em">France has also stepped up the level of vigilance in its procedure to review exportlicence applications in this context. Export licences are issued under the Prime Minister’s responsibility, following the opinion of the interministerial commission for scrutinizing war material exports. The assessment takes particular account of the nature of the material, the end user, respect for human rights in the country of final destination and respect by that country of international humanitarian law, as well as the preservation of peace, security and regional stability. </span><br>
        
