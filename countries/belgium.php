        <p class="font"> <span style="margin-left:4em">TRANSPARENCY</span><br>
            In early 2003 the Belgian Government transferred licensing competence from the Federal Government to the three regional governments (Brussels, Flanders and Wallonia). Along with taking responsibility for granting export licences, the regional  governments are now responsible for reporting on export licences and exports. The Belgian Government has been legally obliged to provide Parliament with an annual report on the implementation of its arms export legislation since 1991. These reports are produced by the Ministry of Economy and include information on licences granted and exports along with information on the type of recipient in each country.<br></p>
        
        <p class="font"> <span style="margin-left:4em">POSITION TOWARDS THE CONFLICT </span><br>
             Belgium’s highest administrative court suspended arms export licenses to Saudi Arabia. The Council of State decided to suspend by an emergency ruling the Wallonian minister-president’s decision on granting licenses to several Belgian companies to export arms to Saudi Arabia. 
            <span style="margin-left:4em">Elio di Rupo, the minister-president of the country’s southern region, gave authorization to two Wallonia-based arms companies, FN Herstal and CMI Defence, to sell weapons to the Saud Arabia’s National Guard and Royal Guard in early July. Licenses were meant to replace previous decisions canceled by the highest administrative court. The new authorizations were again brought to the Council of State by human rights and antiwar associations, claiming Saudi Arabia might use the military equipment to intervene in Yemen. Saudi Arabia is the most important client of the Wallonia’s arms industry. </span><br>
            
