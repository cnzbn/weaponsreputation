            <p class="font"> <span style="margin-left:4em">POSITION TOWARDS THE CONFLICT </span><br>
                 In March 2015, Saudi Arabia established a coalition of nations to engage in military
    operations in Yemen against the Ansar Allah/Houthi movement and loyalists of the
    previous president of Yemen, the late Ali Abdullah Saleh. During 2014, the United
    States joined Saudi Arabia in demanding that Houthi forces reverse their unilateral
    military campaign to occupy the Yemeni capital of Sanaa, but the rapid onset of
    hostilities in March 2015 forced the Obama Administration to react quickly. At the
    start of the Saudi-led intervention on March 25, 2015, the Administration announced
    that the United States would provide “logistical and intelligence support” to the
    coalition’s operations without taking “direct military action in Yemen in support
    of this effort.” Soon thereafter, a joint U.S.- Saudi planning cell was established to
    coordinate military and intelligence support for the campaign. <br>

                <span style="margin-left:4em">At the United Nations
    Security Council, the United States supported the passage of Resolution 2216
    (April 2015), which, among other things, required member states to impose an arms
    embargo against the Houthi-Saleh forces and demanded that the Houthis withdraw
    from all areas seized during the current conflict. The Trump Administration has
    opposed various congressional proposals, including initiatives to reject or condition
    proposed U.S. arms sales or to require an end to U.S. military support to Saudi-led
    coalition operations in Yemen.
