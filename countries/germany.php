        <p class="font"> <span style="margin-left:4em">POSITION TOWARDS THE CONFLICT </span><br>
             The German government had approved an extension of the arms embargo on Saudi Arabia, which will last until December 31, 2020. This decision marks the third extension to the arms embargo, which was initially enacted in late 2018 after the killing of Saudi journalist Jamal Khashoggi, who was assassinated by Saudi agents inside the Saudi consulate in Istanbul, Turkey, in October 2018.  <br>
            <span style="margin-left:4em">However, a number of European arms suppliers have cut arms sales to Saudi Arabia. In addition to Germany, other countries like Switzerland, Finland, Norway, and Denmark cut sales. The German ban has proved to be problematic for the Saudi military, as the country’s industry produces many of the components found in existing systems in the Saudi inventory as well as weapons platforms that Riyadh has been negotiating to buy, such as the Eurofighter Typhoon. After initially pursuing a total arms export ban on Saudi Arabia, Berlin later eased its restrictions to enable the supply of components and spares for completed contracts, but has nevertheless resisted pressure from other large European defense producers, such as the U.K. and France, to lift the embargo on Saudi Arabia.</span><br>
