             The UK is to resume arms sales to Saudi Arabia despite concerns they could be used
against civilians in Yemen, in violation of international humanitarian law. Sales were
suspended in 2019 after a legal challenge by campaigners. A subsequent review found
“isolated incidents” of possible violations but no pattern of non-compliance and “no
clear risk” of future serious breaches. The UK’s support for the Saudi-led international
coalition in Yemen, which is backing the country’s government in its battle against
            a long-running Houthi insurgency, has proved highly controversial. <br>
            <span style="margin-left:4em">According to
campaigners, the UK has licensed £5.3bn worth of arms to Saudi Arabia since 2015.
British officials have provided military advice to the Saudi-led coalition, including on
bombing targets and tactics. Under UK export policy, military equipment licences should
not be granted if there is a “clear risk” that weapons might be used in a “serious violation
                of international humanitarian law”.</span><br>
